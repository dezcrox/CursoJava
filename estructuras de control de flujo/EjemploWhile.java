class EjemploWhile{
    
    public static void main(String args[]){
        //mientras la expresion booleana sea verdadera
        //se repetira el bloque

        int x = 1;
        while(x <= 100){
            System.out.println(x);
            x++;
        }

        System.out.println("Fin de la ejecucion");
    }

}