class Comparacion{
    public static void main(String args[]){
        //comparacion
        double sueldo1 = 10000.51559;
        double sueldo2 = 103034.120912;

        System.out.println(sueldo1 > sueldo2);
        System.out.println(sueldo1 < sueldo2);
        System.out.println(sueldo1 >= sueldo2);
        System.out.println(sueldo1 <= sueldo2);
        System.out.println(sueldo1 != sueldo2);
        System.out.println(sueldo1 == sueldo2);


    }
}